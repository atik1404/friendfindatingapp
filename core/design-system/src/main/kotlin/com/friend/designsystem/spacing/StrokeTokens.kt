package com.friend.designsystem.spacing

import androidx.compose.ui.unit.dp

object StrokeTokens {
    val none = 0.dp        // no border / divider
    val hairline = 0.5.dp    // very thin (list dividers, subtle separators)
    val thin = 1.dp        // standard stroke (input fields, outlines)
    val medium = 2.dp      // emphasized stroke (active borders, cards)
    val thick = 4.dp       // strong emphasis (charts, progress, highlight)
}